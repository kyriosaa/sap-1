; Displays triangle numbers (1+2=3, 1+2+3=6, etc)

LDI 1   ; Load immediate 1              00: (0000   0101 0001)
OUT     ; Output                        01: (0001   1110 0000)
STA 14  ; Store sum                     02: (0010   0100 1110)
STA 15  ; Store increment               03: (0011   0100 1111)
LDA 15  ; Load increment                04: (0100   0001 1111)
ADD 13  ; Add 1                         05: (0101   0010 1101)
STA 15  ; Store new increment           06: (0110   0100 1111)
ADD 14  ; Add to running sum            07: (0111   0010 1110)
OUT     ; Output                        08: (1000   1110 0000)
JC  12  ; If carry, jump to addr 12     09: (1001   0111 1100)
STA 14  ; Store new sum                 10: (1010   0100 1110)
JMP 4   ; Jump to address 4             11: (1011   0110 0100)
HLT     ; Halt                          12: (1100   1111 0000)
        ; 1 (constant)                  13: (1101   0000 0001)
        ; Running sum                   14: (1110   0000 0000)
        ; Current increment             15: (1111   0000 0000)